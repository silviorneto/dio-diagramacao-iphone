package classes;

public abstract class AparelhoTelefonico {
    public void ligar(){
        System.out.println("Aparelho telefônico: ligou...");
    };
    public void atender(){
        System.out.println("Aparelho telefônico: atendeu uma ligação...");
    };
    public void iniciarCorreio(){
        System.out.println("Aparelho telefônico: iniciou correio de voz...");
    };

}
